package com.upic.upicserver;
// A simple Java class representing a LiftRide
public class LiftRide {
  // Private member variables to store skier id and a message
  private int id;

  private String name;

  // Constructor to initialize the LiftRide object with skier id and a message
  public LiftRide(int id, String name) {
    this.id = id;
    this.name = name;
  }
}
